// ===============================
// Modules in Node.js (Short)
// ===============================

// Module means a separate piece of code.
// In Node.js, every file is treated as a module.

// Modules help us to divide big code into small parts.
// This makes code easy to read, manage, and reuse.

// Node.js has mainly three types of modules:

// 1) Core Modules
// These modules are built-in with Node.js.
// No need to install them.
// Examples: fs, http, path, os

// 2) Local Modules
// These are modules created by the user.
// We create our own files and export code from them.