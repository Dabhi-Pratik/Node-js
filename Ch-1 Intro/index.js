// ===============================
// Node.js – History & Introduction
// ===============================

// What is Node.js?
// Node.js is a JavaScript runtime environment.
// It allows us to run JavaScript outside the browser.
// With Node.js, we can build backend servers, APIs, and tools.

// Earlier, JavaScript was only used in browsers.
// We used it mainly for validation, animations, and simple logic.
// Backend work was done using languages like Java, PHP, or Python.

// History of Node.js
// Node.js was created by Ryan Dahl in the year 2009.
// He wanted a faster and more efficient way to handle many users at once.
// Traditional servers were slow when many requests came together.

// Node.js uses Google Chrome’s V8 JavaScript engine.
// V8 converts JavaScript code into fast machine code.
// Because of this, Node.js is very fast.

// One important feature of Node.js is non-blocking I/O.
// This means it does not wait for one task to finish before starting another.
// It can handle many requests at the same time.
// This is very useful for chat apps, APIs, and real-time apps.

// Node.js works on an event-driven model.
// It listens for events and responds when the event happens.
// This helps in saving time and system resources.

// Why Node.js is popular?
// It is fast and scalable.
// It uses JavaScript for both frontend and backend.
// Developers don’t need to learn a new language for backend.
// It is good for real-time applications.

// Where is Node.js used?
// Web servers and APIs
// Chat applications
// Real-time apps like notifications
// Streaming apps
// Backend for web and mobile applications

// ===============================
// What is REPL in Node.js?
// ===============================

// REPL stands for Read, Evaluate, Print, and Loop.
// REPL is an interactive environment in Node.js.

// Read: It reads the code typed by the user.
// Evaluate: It executes the code.
// Print: It shows the output.
// Loop: It waits for the next command.

// We can start REPL by typing `node` in the terminal.
// After that, we can directly write JavaScript code.

// REPL is mainly used for testing small code.
// It is helpful for learning and practice.
// No need to create a file to run code.

// Example use of REPL:
// We can check calculations, functions, or variables quickly.

// Summary
// Node.js allows JavaScript to run outside the browser.
// REPL helps us test and learn JavaScript easily.
